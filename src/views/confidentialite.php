    <h1>Politique de confidentialité</h1>
    <p><strong>Date de dernière mise à jour :</strong> 23 avril 2025</p>

    <h2>1. Qui sommes-nous ?</h2>
    <p>FXGuard est un site web de gestion et partage de configurations de véhicules. Il est exploité par FXGuard Team. Pour toute question, contactez : <a href="mailto:contact@fxguard.com">contact@fxguard.com</a>.</p>

    <h2>2. Quelles données personnelles collectons-nous ?</h2>
    <ul>
        <li>Données d’identification : nom d'utilisateur, email</li>
        <li>Données de connexion : adresse IP, logs, navigateur, date d’accès</li>
        <li>Données d’interaction : listes créées, véhicules ajoutés, commentaires</li>
        <li>Données techniques : type de navigateur, système d'exploitation</li>
    </ul>

    <h2>3. Pourquoi collectons-nous ces données ?</h2>
    <ul>
        <li>Création et gestion des comptes utilisateurs</li>
        <li>Affichage personnalisé du contenu</li>
        <li>Suivi des interactions communautaires</li>
        <li>Sécurisation du site (lutte contre les fraudes, accès non autorisés)</li>
        <li>Statistiques anonymisées (à usage interne uniquement)</li>
    </ul>

    <h2>4. Cookies</h2>
    <p>FXGuard utilise uniquement des cookies fonctionnels nécessaires au fonctionnement du site. Aucun cookie publicitaire n’est utilisé sans votre consentement.</p>

    <h2>5. Base légale du traitement</h2>
    <p>Conformément au RGPD, nous traitons vos données sur les bases suivantes :</p>
    <ul>
        <li>Consentement (ex: inscription, contact)</li>
        <li>Contrat (utilisation du site)</li>
        <li>Obligation légale (sécurité, archivage)</li>
        <li>Intérêt légitime (amélioration du service, prévention de fraudes)</li>
    </ul>

    <h2>6. Qui a accès à vos données ?</h2>
    <p>Seuls les administrateurs de FXGuard ont accès aux données. Aucun partage avec des tiers sans votre consentement explicite.</p>

    <h2>7. Combien de temps conservons-nous vos données ?</h2>
    <ul>
        <li>Comptes utilisateurs : jusqu’à suppression du compte</li>
        <li>Données de navigation : 12 mois maximum</li>
        <li>Données de logs : 6 mois maximum</li>
    </ul>

